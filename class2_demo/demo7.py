#python 中运算符执行的优先级

#有括号则先计算括号中的内容
#有幂运算的先进行幂运算，再先算乘除，后算加减                输出结果为数字
#位运算中，先算左右位移运算<< >> ,再算或 &，后算与 |        输出结果为数字
#再往下则是比较运算符   >,<,>=,<=,==,!==                输出结果为true false
#再往下则是布尔运算符  and,or,not,in,not in             输出结果为 true false
#再往下则是赋值运算符   =                               用于进行赋值操作